        aws.getRemotePythonPID(); % Conceptually, bgTrnWorkerObj should
            % remember. Right now there is only one PID per aws so it's ok
        
        obj.trnName = modelChainID;
        obj.trnNameLbl = trainID;
        obj.trnLastDMC = dmc;

      end
