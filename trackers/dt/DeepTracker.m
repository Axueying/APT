          tblMFTRetrack = obj.getTrackedMFT();
          obj.track(tblMFTRetrack);
        end

      end
	  % completed/stopped training. old tracking results are deleted/updated, so trackerInfo should be updated
      obj.updateTrackerInfo();
    end
    
